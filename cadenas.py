#Para poder imprimir las " " en un programa se puede hacer de dos formas

##La primera \"
print("Para poder imprimir las comillas se puede hacer \"asi\"")

##La segunta '""'
print('Para poder imprimir las comillas se puede hacer "asi"')

#De igual manera se puede hacer con las apostrofes

##La primera \'
print("I\'m Sebastian")

##La segunda "''"
print("I'm Sebastian")