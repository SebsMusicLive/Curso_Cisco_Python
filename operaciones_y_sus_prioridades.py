#Operadores y sus enlaces
""" El enlace de un operador determina el orden en que se computan las operaciones 
de los operadores con la misma prioridad, los cuales se encuentran 
dentro de una misma expresión.
La mayoría de los operadores de Python tienen un enlazado 
hacia la izquierda, lo que significa que el cálculo 
de la expresión es realizado de izquierda a derecha. """
print('Enlace:',9 % 6 % 2)

